def classify_band_simple(freq_hz: int) -> str:
    """Classify frequency into band name."""
    mhz = freq_hz / 1_000_000
    if 144 <= mhz <= 148:
        return "2m"
    if 222 <= mhz <= 225:
        return "1.25m"
    if 420 <= mhz <= 450:
        return "70cm"
    if 50 <= mhz <= 54:
        return "6m"
    if mhz < 30:
        return "hf"
    return "other"


