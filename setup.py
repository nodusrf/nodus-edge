def is_raspberry_pi() -> bool:
    """Detect if running on a Raspberry Pi (too slow for local Whisper)."""
    import platform
    arch = platform.machine()
    if arch not in ("aarch64", "armv7l"):
        return False
    # Check /proc/cpuinfo for Raspberry Pi
    try:
        with open("/proc/cpuinfo") as f:
            cpuinfo = f.read()
        if "Raspberry Pi" in cpuinfo or "BCM2" in cpuinfo:
            return True
    except OSError:
        pass
    # Check device-tree model
    try:
        with open("/proc/device-tree/model") as f:
            model = f.read()
        if "Raspberry Pi" in model:
            return True
    except OSError:
        pass
    # Fallback: aarch64 with < 4GB RAM is likely a Pi or similar SBC
    try:
        with open("/proc/meminfo") as f:
            for line in f:
                if line.startswith("MemTotal:"):
                    mem_kb = int(line.split()[1])
                    if mem_kb < 4_000_000:
                        return True
                    break
    except (OSError, ValueError):
        pass
    return False


