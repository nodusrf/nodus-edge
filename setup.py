def _sanitize_callsign(raw: str) -> str:
    """Strip a callsign to alphanumeric only. Returns empty on garbage."""
    clean = re.sub(r"[^A-Za-z0-9]", "", raw).upper()
    if len(clean) < 3 or len(clean) > 7:
        return ""
    return clean


def ask_callsign(args) -> str:
    """Ask for operator callsign (optional)."""
    if args.non_interactive:
        return _sanitize_callsign(args.callsign or "")

    print()
    print(f"  {BOLD}Step 3: Callsign{NC}")
    print(f"  {'─' * 50}")
    print()

    callsign = prompt("What is your callsign? (optional, Enter to skip)")

    if not callsign:
        info("No callsign — node will be anonymous.")
        return ""

    callsign = callsign.upper()

    if CALLSIGN_RE.match(callsign):
        info(f"Callsign: {callsign}")
        return callsign

    if CALLSIGN_BROAD_RE.match(callsign):
        warn(f"{callsign} doesn't match standard US callsign format.")
        if prompt_yn("Use it anyway?"):
            return callsign
        return ""

    warn(f"{callsign} doesn't look like a valid callsign.")
    if prompt_yn("Use it anyway?", default_yes=False):
        return callsign
    return ""


