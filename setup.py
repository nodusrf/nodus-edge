def resolve_zip(zip_code: str) -> dict:
    """Resolve zip code via zippopotam.us API."""
    url = ZIPPOPOTAM_API.format(zip=zip_code)
    req = Request(url)
    req.add_header("User-Agent", USER_AGENT)

    with urlopen(req, timeout=10) as response:
        data = json.loads(response.read().decode("utf-8"))

    place = data["places"][0]
    return {
        "lat": float(place["latitude"]),
        "lon": float(place["longitude"]),
        "city": place["place name"],
        "state": place["state"],
        "state_abbrev": place["state abbreviation"],
    }


def ask_manual_location() -> dict:
    """Fall back to manual location entry."""
    print()
    warn("Enter your location manually:")

    def validate_float(v):
        try:
            float(v)
            return None
        except ValueError:
            return "Must be a number."

    def validate_state(v):
        if v.upper() not in STATE_ABBREV_TO_NAME:
            return "Unknown state abbreviation."
        return None

    lat = float(prompt("Latitude", validator=validate_float))
    lon = float(prompt("Longitude", validator=validate_float))
    city = prompt("City")
    state_abbrev = prompt("State abbreviation (e.g. AZ)", validator=validate_state).upper()
    state = STATE_ABBREV_TO_NAME[state_abbrev]

    return {
        "lat": lat,
        "lon": lon,
        "city": city,
        "state": state,
        "state_abbrev": state_abbrev,
    }


def ask_location(args, zip_metro: dict) -> dict:
    """Ask for zip code, resolve to location + metro."""
    if args.non_interactive:
        # Non-interactive: use CLI args
        if args.lat and args.lon and args.city and args.state:
            state_name = STATE_ABBREV_TO_NAME.get(args.state.upper(), args.state)
            loc = {
                "lat": args.lat, "lon": args.lon,
                "city": args.city, "state": state_name,
                "state_abbrev": args.state.upper(),
            }
            zip_code = args.zip or ""
        elif args.zip:
            if not ZIP_RE.match(args.zip):
                error(f"Invalid zip code: {args.zip} (must be exactly 5 digits)")
                sys.exit(1)
            try:
                loc = resolve_zip(args.zip)
            except HTTPError as e:
                if e.code == 404:
                    error(f"Zip code {args.zip} not found.")
                else:
                    error(f"Zip code API error: {e.code}")
                sys.exit(1)
            except (URLError, Exception) as e:
                error(f"Could not reach zip code API: {e}")
                error("Use --lat/--lon/--city/--state instead.")
                sys.exit(1)
            zip_code = args.zip
        else:
            error("Non-interactive mode requires --zip or --lat/--lon/--city/--state")
            sys.exit(1)

        metro = args.metro or lookup_metro(zip_code, loc["city"], zip_metro)
        loc["metro"] = metro
        return loc

    print()
    print(f"  {BOLD}Step 2: Location{NC}")
    print(f"  {'─' * 50}")
    print()

    zip_code = ""
    while True:
        zip_code = prompt("What is your zip code? (or 'manual' for manual entry)")

        if zip_code.lower() == "manual":
            loc = ask_manual_location()
            zip_code = ""
            break

        if not ZIP_RE.match(zip_code):
            error("Zip code must be exactly 5 digits.")
            continue

        try:
            loc = resolve_zip(zip_code)
            break
        except HTTPError as e:
            if e.code == 404:
                error(f"Zip code {zip_code} not found.")
            else:
                error(f"API error: {e.code}")
                if prompt_yn("Enter location manually?"):
                    loc = ask_manual_location()
                    zip_code = ""
                    break
        except (URLError, Exception) as e:
            warn(f"Could not reach zip code API: {e}")
            if prompt_yn("Enter location manually?"):
                loc = ask_manual_location()
                zip_code = ""
                break

    info(f"Location: {loc['city']}, {loc.get('state_abbrev', '')} ({loc['lat']:.4f}, {loc['lon']:.4f})")
    print()

    # Resolve metro via CBSA mapping, fall back to city name
    default_metro = lookup_metro(zip_code, loc["city"], zip_metro)
    cbsa_source = " (CBSA)" if zip_code in zip_metro else ""
    print(f"  Your metro area will be: {BOLD}{default_metro}{NC}{cbsa_source} ({loc['city']}, {loc.get('state_abbrev', '')})")
    choice = prompt("Is this correct? (y/n/custom metro name)", default="y")

    if choice.lower() in ("y", "yes", ""):
        loc["metro"] = default_metro
    elif choice.lower() in ("n", "no"):
        custom = prompt("Enter metro name")
        loc["metro"] = slugify_city(custom)
    else:
        # They typed a custom metro name directly
        loc["metro"] = slugify_city(choice)

    info(f"Metro: {loc['metro']}")
    return loc


